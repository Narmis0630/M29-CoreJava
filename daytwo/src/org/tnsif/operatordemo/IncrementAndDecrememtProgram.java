//program to demonstrate pre and post increment and decrement Operator
package org.tnsif.operatordemo;
public class IncrementAndDecrememtProgram {

	public static void main(String[] args) {
		int a=12;
		int x= a++;
		++a;
		System.out.println(a);
		System.out.println (x);
		a++;
		System.out.println (a);
		System.out.println (x);

	}

}	
